package autoboxing_and_unboxing_exercise;


	public class IntegerOperations {
	    public static void main(String[] args) {
	        int a = 5;
	        int b = 3;
	        int sum = a + b;
	        System.out.println("Sum using primitive data types: " + sum);

	        Integer x = 10;
	        Integer y = 7;
	        int subtract = x - y;
	        System.out.println("Subtraction using wrapper classes: " + subtract);

	        int c = 4;
	        int d = 6;
	        int multiply = c * d;
	        System.out.println("Multiplication using primitive data types: " + multiply);

	        Integer p = 20;
	        Integer q = 5;
	        int division = p / q;
	        System.out.println("Division using wrapper classes: " + division);
	    }
	}
